"""
conditions.py — Think AI Live Condition Monitor
=================================================
Responsibility:  Store, track, and evaluate the Go/Stop/Review conditions
                 that the Refiner generates for every decision. Turns a
                 static analysis snapshot into a living decision system.

The problem this solves
-----------------------
Without conditions.py, the Go/Stop/Review conditions inside VerdictBox
are printed once and forgotten. A company makes a decision on Monday,
a stop condition fires on Wednesday, and nobody knows.

This module makes conditions actionable:
  1. register_conditions()  — called after every /decide, stores conditions
  2. check_conditions()     — evaluates each condition's current status
  3. get_active_conditions() — returns everything live for a company
  4. acknowledge_condition() — user marks a fired condition as seen
  5. request_reanalysis()    — escalates a fired stop condition back through
                               the full route → refine → format_for_ui pipeline

How conditions are evaluated
-----------------------------
Conditions are free-text strings generated by the Refiner (e.g.
"Go ahead if churn rate is below 5%"). Full automated evaluation
requires live data feeds — that's marketplace.py's job.

Until marketplace.py is built, conditions.py evaluates in two ways:
  * Time-based triggers  — "Review again in 30 days" → fires automatically
  * Manual acknowledgement — user reads conditions and marks them
  * LLM-assisted spot-check — a lightweight LLM call checks if a
    condition MIGHT have changed based on context the user provides

This is the honest architecture: useful now, fully automated later.

How it connects (zero edits to existing files)
-----------------------------------------------
  main.py  →  register_conditions()   after update_memory()
  main.py  →  new /conditions endpoints
  conditions.py imports UIDecisionPayload, VerdictBox from refiner/output
  memory.py is NOT imported — conditions has its own storage to keep
  concerns separated and allow independent scaling.

Storage
-------
  Default  : JSON files in conditions_store/ (same pattern as memory.py)
  Production: Swap _ConditionBackend for Redis / Postgres via env var.

Security
--------
  * company_id SHA-256 hashed in all file paths — no PII on disk
  * decision_id validated before any write
  * Condition text sanitised before LLM spot-check calls
  * Per-company asyncio locks on all writes
  * LLM spot-check is read-only — never modifies a decision

Usage
-----
    from conditions import register_conditions, get_active_conditions

    # After /decide (called from main.py)
    await register_conditions(company_id, decision_id, query, verdict_box)

    # Polling or webhook — check what's fired
    report = await get_active_conditions(company_id)
"""

