class ConditionRecord(BaseModel):
    """One stored condition attached to a decision."""

    condition_id:   str                  # SHA-256 of decision_id + kind + text
    decision_id:    str
    company_id:     str
    query_preview:  str                  # first 80 chars of the original query
    kind:           ConditionKind
    text:           str                  # the raw condition string from Refiner
    status:         ConditionStatus      = "active"
    created_utc:    str
    last_checked_utc: str | None         = None
    fired_utc:      str | None           = None
    acknowledged_utc: str | None         = None
    cleared_utc:    str | None           = None
    expires_utc:    str                  # auto-set to created + _AUTO_REVIEW_DAYS
    spot_check_note: str | None          = None  # LLM's last assessment
    reanalysis_requested: bool           = False

    @field_validator("kind")
    @classmethod
    def validate_kind(cls, v: str) -> str:
        if v not in ("go", "stop", "review"):
            return "review"
        return v

    @field_validator("status")
    @classmethod
    def validate_status(cls, v: str) -> str:
        valid = ("active", "fired", "acknowledged", "cleared", "expired")
        return v if v in valid else "active"


class CompanyConditionStore(BaseModel):
    """All conditions for one company — what gets written to disk."""
    company_id_hash: str
    version:         str = _CONDITIONS_VERSION
    records:         list[ConditionRecord] = Field(default_factory=list)
    last_updated_utc: str


class ConditionReport(BaseModel):
    """
    Client-facing report returned by get_active_conditions().
    Safe to send directly to the frontend.
    """
    company_id:       str
    total_active:     int
    total_fired:      int
    total_acknowledged: int
    urgent:           list[ConditionRecord]   # fired stop conditions
    needs_review:     list[ConditionRecord]   # fired review triggers
    go_conditions:    list[ConditionRecord]   # go conditions (active or fired)
    generated_utc:    str


class SpotCheckRequest(BaseModel):
    """Body for POST /conditions/spot-check."""
    company_id:   str = Field(min_length=1, max_length=128)
    condition_id: str = Field(min_length=1, max_length=64)
    user_update:  str = Field(
        min_length=3, max_length=1000,
        description=(
            "What the user knows right now that's relevant to this condition. "
            "e.g. 'Our churn rate went up to 7% this month.'"
        ),
    )
    context: dict[str, Any] | None = None


class SpotCheckResult(BaseModel):
    """Result of an LLM-assisted condition spot-check."""
    condition_id:    str
    condition_text:  str
    kind:            ConditionKind
    user_update:     str
    assessment:      Literal["likely_fired", "likely_clear", "uncertain"]
    reasoning:       str
    recommended_action: str
    new_status:      ConditionStatus   # what we set the condition to after this check


