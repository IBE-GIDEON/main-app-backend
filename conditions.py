async def register_conditions(
    company_id:  str,
    decision_id: str,
    query:       str,
    verdict:     VerdictBox,
) -> bool:
    """
    Called by main.py after format_for_ui() — as a fire-and-forget task.
    Extracts go/stop/review conditions from the VerdictBox and stores them.
    Safe to call multiple times for the same decision (deduplicates by ID).

    Parameters
    ----------
    company_id  : Tenant identifier.
    decision_id : Unique ID for this decision (from AuditTrail.query_hash).
    query       : The original decision text.
    verdict     : The VerdictBox from the RefinedDecision.

    Returns
    -------
    bool — True if written successfully.
    """
    new_records = _build_records_from_verdict(company_id, decision_id, query, verdict)

    if not new_records:
        logger.info(
            "No conditions to register | company_hash=%s | decision=%s",
            _company_hash(company_id)[:12], decision_id,
        )
        return True

    lock = _get_lock(company_id)
    try:
        await asyncio.wait_for(lock.acquire(), timeout=_LOCK_TIMEOUT_SEC)
    except asyncio.TimeoutError:
        logger.warning(
            "Conditions register lock timeout | company_hash=%s",
            _company_hash(company_id)[:12],
        )
        return False

    try:
        t0    = time.perf_counter()
        store = await _read_store(company_id) or _new_store(company_id)

        # Deduplicate — never double-register same condition
        existing_ids = {r.condition_id for r in store.records}
        to_add       = [r for r in new_records if r.condition_id not in existing_ids]

        store.records.extend(to_add)
        store.last_updated_utc = datetime.now(timezone.utc).isoformat()

        success = await _write_store(company_id, store)
        latency_ms = round((time.perf_counter() - t0) * 1000)

        logger.info(
            "✅ Conditions registered | company_hash=%s | decision=%s | "
            "added=%d | total=%d | latency_ms=%d",
            _company_hash(company_id)[:12],
            decision_id,
            len(to_add),
            len(store.records),
            latency_ms,
        )
        return success

    except Exception as e:
        logger.error(
            "register_conditions error | company_hash=%s | %s",
            _company_hash(company_id)[:12], e,
        )
        return False
    finally:
        lock.release()


async def get_active_conditions(company_id: str) -> ConditionReport:
    """
    Returns a structured report of all live conditions for a company.
    Automatically expires stale conditions as part of the read.
    Safe to call as frequently as needed — read-only except for expiry updates.

    Returns
    -------
    ConditionReport — always, even if empty.
    """
    t0    = time.perf_counter()
    store = await _read_store(company_id)

    if not store:
        now = datetime.now(timezone.utc).isoformat()
        return ConditionReport(
            company_id=company_id,
            total_active=0,
            total_fired=0,
            total_acknowledged=0,
            urgent=[],
            needs_review=[],
            go_conditions=[],
            generated_utc=now,
        )

    # Run expiry check on every record — update store if anything changed
    updated = [_check_expiry(r) for r in store.records]
    if any(r.status == "expired" for r in updated):
        store.records = updated
        store.last_updated_utc = datetime.now(timezone.utc).isoformat()
        # Non-blocking write — if it fails, next call will re-expire
        asyncio.create_task(_write_store(company_id, store))

    active  = [r for r in store.records if r.status == "active"]
    fired   = [r for r in store.records if r.status == "fired"]
    acked   = [r for r in store.records if r.status == "acknowledged"]

    # Sort by priority within each group
    urgent       = sorted(
        [r for r in fired if r.kind == "stop"],
        key=lambda r: r.fired_utc or r.created_utc,
    )
    needs_review = sorted(
        [r for r in fired if r.kind in ("review", "go")],
        key=lambda r: r.fired_utc or r.created_utc,
    )
    go_all       = sorted(
        [r for r in store.records if r.kind == "go" and r.status in ("active", "fired")],
        key=lambda r: r.status,   # fired first
    )

    latency_ms = round((time.perf_counter() - t0) * 1000)
    logger.info(
        "Conditions report | company_hash=%s | active=%d | fired=%d | latency_ms=%d",
        _company_hash(company_id)[:12],
        len(active), len(fired), latency_ms,
    )

    return ConditionReport(
        company_id=company_id,
        total_active=len(active),
        total_fired=len(fired),
        total_acknowledged=len(acked),
        urgent=urgent,
        needs_review=needs_review,
        go_conditions=go_all,
        generated_utc=datetime.now(timezone.utc).isoformat(),
    )


async def acknowledge_condition(
    company_id:   str,
    condition_id: str,
) -> bool:
    """
    User has seen the alert for a fired condition.
    Moves status from 'fired' → 'acknowledged'.
    Does NOT clear it — the condition is still real, just seen.

    Returns
    -------
    bool — True if found and updated.
    """
    return await _update_condition_status(
        company_id=company_id,
        condition_id=condition_id,
        new_status="acknowledged",
        timestamp_field="acknowledged_utc",
    )


async def clear_condition(
    company_id:   str,
    condition_id: str,
) -> bool:
    """
    User confirms a condition is no longer relevant (e.g. the risk passed).
    Moves status to 'cleared'.

    Returns
    -------
    bool — True if found and updated.
    """
    return await _update_condition_status(
        company_id=company_id,
        condition_id=condition_id,
        new_status="cleared",
        timestamp_field="cleared_utc",
    )


async def fire_condition(
    company_id:   str,
    condition_id: str,
) -> bool:
    """
    Manually mark a condition as fired (used by spot-check and marketplace.py).
    Moves status to 'fired'.

    Returns
    -------
    bool — True if found and updated.
    """
    return await _update_condition_status(
        company_id=company_id,
        condition_id=condition_id,
        new_status="fired",
        timestamp_field="fired_utc",
    )


async def _update_condition_status(
    company_id:      str,
    condition_id:    str,
    new_status:      ConditionStatus,
    timestamp_field: str,
) -> bool:
    """Internal helper — updates one condition's status field."""
    lock = _get_lock(company_id)
    try:
        await asyncio.wait_for(lock.acquire(), timeout=_LOCK_TIMEOUT_SEC)
    except asyncio.TimeoutError:
        return False

    try:
        store = await _read_store(company_id)
        if not store:
            return False

        for record in store.records:
            if record.condition_id == condition_id:
                record.status = new_status
                setattr(record, timestamp_field, datetime.now(timezone.utc).isoformat())
                record.last_checked_utc = datetime.now(timezone.utc).isoformat()
                store.last_updated_utc  = datetime.now(timezone.utc).isoformat()

                success = await _write_store(company_id, store)
                logger.info(
                    "✅ Condition updated | company_hash=%s | id=%s | status=%s",
                    _company_hash(company_id)[:12], condition_id, new_status,
                )
                return success

        logger.warning(
            "Condition not found | company_hash=%s | id=%s",
            _company_hash(company_id)[:12], condition_id,
        )
        return False

    except Exception as e:
        logger.error(
            "_update_condition_status error | company_hash=%s | %s",
            _company_hash(company_id)[:12], e,
        )
        return False
    finally:
        lock.release()


async def run_spot_check(
    company_id:   str,
    condition_id: str,
    user_update:  str,
    context:      UserContext | None = None,
) -> SpotCheckResult | None:
    """
    LLM-assisted condition evaluation.
    User provides a business update; the LLM assesses whether the condition
    has likely fired, is clear, or is uncertain.
    Updates the condition's status and stores the LLM's note.

    Parameters
    ----------
    company_id   : Tenant identifier.
    condition_id : The condition to check.
    user_update  : What the user knows right now about their business.
    context      : Optional UserContext for additional enrichment.

    Returns
    -------
    SpotCheckResult or None if condition not found.
    """
    # Find the condition
    store = await _read_store(company_id)
    if not store:
        return None

    target: ConditionRecord | None = None
    for r in store.records:
        if r.condition_id == condition_id:
            target = r
            break

    if target is None:
        logger.warning(
            "Spot-check: condition not found | company_hash=%s | id=%s",
            _company_hash(company_id)[:12], condition_id,
        )
        return None

    if target.status in ("cleared", "expired"):
        logger.info(
            "Spot-check skipped — condition already %s | id=%s",
            target.status, condition_id,
        )
        return None

    logger.info(
        "🔍 Spot-check | company_hash=%s | id=%s | kind=%s",
        _company_hash(company_id)[:12], condition_id, target.kind,
    )

    try:
        result = await _run_spot_check(target, user_update)
    except RuntimeError as e:
        logger.error("Spot-check LLM failed | %s", e)
        return None

    # Map assessment → new status
    status_map: dict[str, ConditionStatus] = {
        "likely_fired": "fired",
        "likely_clear": "active",
        "uncertain":    "active",
    }
    new_status = status_map[result.assessment]

    # Update stored record
    lock = _get_lock(company_id)
    try:
        await asyncio.wait_for(lock.acquire(), timeout=_LOCK_TIMEOUT_SEC)
    except asyncio.TimeoutError:
        logger.warning("Spot-check lock timeout | company_hash=%s",
                       _company_hash(company_id)[:12])
    else:
        try:
            store2 = await _read_store(company_id)
            if store2:
                for r in store2.records:
                    if r.condition_id == condition_id:
                        r.status            = new_status
                        r.last_checked_utc  = datetime.now(timezone.utc).isoformat()
                        r.spot_check_note   = result.reasoning
                        if new_status == "fired":
                            r.fired_utc = datetime.now(timezone.utc).isoformat()
                        break
                store2.last_updated_utc = datetime.now(timezone.utc).isoformat()
                await _write_store(company_id, store2)
        except Exception as e:
            logger.error("Spot-check write error | %s", e)
        finally:
            lock.release()

    logger.info(
        "✅ Spot-check done | company_hash=%s | id=%s | assessment=%s | new_status=%s",
        _company_hash(company_id)[:12], condition_id,
        result.assessment, new_status,
    )

    return SpotCheckResult(
        condition_id=condition_id,
        condition_text=target.text,
        kind=target.kind,
        user_update=user_update,
        assessment=result.assessment,
        reasoning=result.reasoning,
        recommended_action=result.recommended_action,
        new_status=new_status,
    )


async def request_reanalysis(
    company_id:   str,
    condition_id: str,
) -> bool:
    """
    Flags a condition for full re-analysis.
    Sets reanalysis_requested=True on the record.
    main.py / a background job can poll for these and re-run /decide.
    This is the hook for marketplace.py's automated re-triggering.

    Returns
    -------
    bool — True if flagged successfully.
    """
    lock = _get_lock(company_id)
    try:
        await asyncio.wait_for(lock.acquire(), timeout=_LOCK_TIMEOUT_SEC)
    except asyncio.TimeoutError:
        return False

    try:
        store = await _read_store(company_id)
        if not store:
            return False

        for record in store.records:
            if record.condition_id == condition_id:
                record.reanalysis_requested = True
                record.last_checked_utc     = datetime.now(timezone.utc).isoformat()
                store.last_updated_utc      = datetime.now(timezone.utc).isoformat()
                success = await _write_store(company_id, store)
                logger.info(
                    "🔁 Re-analysis requested | company_hash=%s | id=%s",
                    _company_hash(company_id)[:12], condition_id,
                )
                return success

        return False

    except Exception as e:
        logger.error("request_reanalysis error | %s", e)
        return False
    finally:
        lock.release()


async def get_reanalysis_queue(company_id: str) -> list[ConditionRecord]:
    """
    Returns all conditions where reanalysis_requested=True.
    Called by marketplace.py (and optionally by a background scheduler)
    to know which decisions need re-running through the full pipeline.
    """
    store = await _read_store(company_id)
    if not store:
        return []
    return [r for r in store.records if r.reanalysis_requested]


async def clear_all_conditions(company_id: str) -> bool:
    """
    Wipes all conditions for a company.
    Called alongside memory.clear_memory() for GDPR erasure.
    """
    lock = _get_lock(company_id)
    try:
        await asyncio.wait_for(lock.acquire(), timeout=_LOCK_TIMEOUT_SEC)
    except asyncio.TimeoutError:
        return False

    try:
        path = _store_path(company_id)
        if path.exists():
            await asyncio.to_thread(path.unlink)
            logger.info(
                "🗑️  Conditions cleared | company_hash=%s",
                _company_hash(company_id)[:12],
            )
            return True
        return False
    except Exception as e:
        logger.error("clear_all_conditions error | %s", e)
        return False
    finally:
        lock.release()


