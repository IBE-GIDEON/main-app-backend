ConditionKind   = Literal["go", "stop", "review"]
ConditionStatus = Literal[
    "active",       # condition is live, not yet evaluated
    "fired",        # condition has triggered — needs attention
    "acknowledged", # user has seen the alert
    "cleared",      # condition is no longer relevant (manual or auto)
    "expired",      # decision is old enough that conditions are stale
]

# Stop conditions are always urgent.
# Go conditions: if one fails, the decision turns Orange.
# Review triggers: fire on a schedule or when context changes.
STATUS_PRIORITY: dict[ConditionStatus, int] = {
    "fired":        1,
    "active":       2,
    "acknowledged": 3,
    "cleared":      4,
    "expired":      5,
}


