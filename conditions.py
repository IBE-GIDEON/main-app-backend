_write_locks: dict[str, asyncio.Lock] = {}


def _get_lock(company_id: str) -> asyncio.Lock:
    if company_id not in _write_locks:
        _write_locks[company_id] = asyncio.Lock()
    return _write_locks[company_id]


# ─────────────────────────────────────────────
# STORAGE BACKEND
# ─────────────────────────────────────────────

def _company_hash(company_id: str) -> str:
    return hashlib.sha256(company_id.encode()).hexdigest()


def _store_path(company_id: str) -> Path:
    return _STORE_DIR / f"{_company_hash(company_id)}.json"


def _ensure_store_dir() -> None:
    _STORE_DIR.mkdir(parents=True, exist_ok=True)
    gitignore = _STORE_DIR / ".gitignore"
    if not gitignore.exists():
        gitignore.write_text("*\n")


async def _read_store(company_id: str) -> CompanyConditionStore | None:
    path = _store_path(company_id)
    if not path.exists():
        return None
    try:
        raw = await asyncio.to_thread(path.read_text, encoding="utf-8")
        return CompanyConditionStore.model_validate_json(raw)
    except Exception as e:
        logger.error(
            "Conditions read error | company_hash=%s | %s",
            _company_hash(company_id)[:12], e,
        )
        return None


async def _write_store(company_id: str, store: CompanyConditionStore) -> bool:
    _ensure_store_dir()
    path = _store_path(company_id)
    # Cap total records — drop oldest cleared/expired first
    if len(store.records) > _MAX_RECORDS:
        droppable = [
            r for r in store.records
            if r.status in ("cleared", "expired", "acknowledged")
        ]
        droppable.sort(key=lambda r: r.created_utc)
        to_drop = set(r.condition_id for r in droppable[:len(store.records) - _MAX_RECORDS])
        store.records = [r for r in store.records if r.condition_id not in to_drop]

    try:
        await asyncio.to_thread(
            path.write_text,
            store.model_dump_json(indent=2),
            encoding="utf-8",
        )
        return True
    except Exception as e:
        logger.error(
            "Conditions write error | company_hash=%s | %s",
            _company_hash(company_id)[:12], e,
        )
        return False


def _new_store(company_id: str) -> CompanyConditionStore:
    return CompanyConditionStore(
        company_id_hash=_company_hash(company_id),
        records=[],
        last_updated_utc=datetime.now(timezone.utc).isoformat(),
    )


# ─────────────────────────────────────────────
# INTERNAL HELPERS
# ─────────────────────────────────────────────

def _condition_id(decision_id: str, kind: str, text: str) -> str:
    """Deterministic, stable ID for one condition."""
    raw = f"{decision_id}:{kind}:{text.strip().lower()}"
    return hashlib.sha256(raw.encode()).hexdigest()[:16]


def _expires_utc(days: int = _AUTO_REVIEW_DAYS) -> str:
    return (datetime.now(timezone.utc) + timedelta(days=days)).isoformat()


def _check_expiry(record: ConditionRecord) -> ConditionRecord:
    """If the condition's expiry has passed and it's still active, mark expired."""
    if record.status not in ("active", "fired"):
        return record
    try:
        expires = datetime.fromisoformat(record.expires_utc)
        if datetime.now(timezone.utc) > expires:
            record.status = "expired"
            logger.info(
                "Condition expired | id=%s | kind=%s",
                record.condition_id, record.kind,
            )
    except Exception:
        pass
    return record


def _build_records_from_verdict(
    company_id:   str,
    decision_id:  str,
    query:        str,
    verdict:      VerdictBox,
) -> list[ConditionRecord]:
    """
    Converts a VerdictBox's three condition lists into ConditionRecord objects.
    Deduplicates by condition_id so re-registering the same decision is safe.
    """
    now = datetime.now(timezone.utc).isoformat()
    records: list[ConditionRecord] = []

    # Review triggers expire sooner — they're time-sensitive by nature
    expiry_days: dict[str, int] = {
        "go":     _AUTO_REVIEW_DAYS,
        "stop":   _AUTO_REVIEW_DAYS * 2,    # stop conditions stay longer
        "review": max(7, _AUTO_REVIEW_DAYS // 2),
    }

    for kind, texts in [
        ("go",     verdict.go_conditions),
        ("stop",   verdict.stop_conditions),
        ("review", verdict.review_triggers),
    ]:
        for text in texts:
            if not text or not text.strip():
                continue
            cid = _condition_id(decision_id, kind, text)
            records.append(ConditionRecord(
                condition_id=cid,
                decision_id=decision_id,
                company_id=company_id,
                query_preview=query[:80],
                kind=kind,
                text=text.strip(),
                status="active",
                created_utc=now,
                expires_utc=_expires_utc(expiry_days[kind]),
            ))

    return records


# ─────────────────────────────────────────────
# SPOT-CHECK LLM CALL
# Lightweight — one call, no 3-pass loop needed.
# Just asks: has this condition likely fired?
# ─────────────────────────────────────────────

_SPOT_CHECK_SYSTEM = """
You are Think AI's Condition Evaluator.
A user has provided an update about their business situation.
Your job is to assess whether a specific decision condition has likely fired,
is likely still clear, or is uncertain.

Be direct and specific. Do not hedge everything.
Return valid JSON only.
""".strip()


class _SpotCheckOutput(BaseModel):
    assessment:          Literal["likely_fired", "likely_clear", "uncertain"]
    reasoning:           str = Field(description="2–3 sentences explaining your assessment")
    recommended_action:  str = Field(description="One concrete action the user should take now")


async def _run_spot_check(
    condition:   ConditionRecord,
    user_update: str,
) -> _SpotCheckOutput:
    """Single LLM call to evaluate whether a condition has fired."""
    last_error: Exception | None = None

    user_message = f"""
Original decision: {condition.query_preview}

Condition type: {condition.kind.upper()}
Condition text: {condition.text}

User's current update: {user_update}

Has this condition fired, is it still clear, or is it uncertain?
""".strip()

    for attempt in range(1, _MAX_RETRIES + 1):
        try:
            response = await _get_client().chat.completions.create(
                model=_SPOT_CHECK_MODEL,
                messages=[
                    {"role": "system", "content": _SPOT_CHECK_SYSTEM + "\n\nRespond with valid JSON only. No markdown."},
                    {"role": "user",   "content": user_message},
                ],
                response_format={"type": "json_object"},
                temperature=0,
            )

            msg = response.choices[0].message
            raw = (msg.content or "").strip().lstrip("```json").lstrip("```").rstrip("```").strip()
            import json as _json
            return _SpotCheckOutput.model_validate(_json.loads(raw))

        except (APITimeoutError, APIStatusError) as e:
            status_code = getattr(e, "status_code", None)
            if status_code and status_code not in (429, 500, 502, 503, 504):
                raise
            last_error = e
            wait = (2 ** attempt) + random.uniform(0, 0.5)
            logger.warning(
                "Spot-check transient error | attempt=%d/%d | wait=%.1fs | %s",
                attempt, _MAX_RETRIES, wait, e,
            )
            await asyncio.sleep(wait)

        except Exception as e:
            last_error = e
            break

    raise RuntimeError("Spot-check failed after retries") from last_error


